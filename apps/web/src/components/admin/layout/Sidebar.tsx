'use client'

import { useState, useEffect } from 'react'
import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { useAdmin } from '@/contexts/AdminContext'
import { SIDEBAR_MENU, filterMenuByPermissions, MenuItem } from '@/types/admin'

interface SidebarProps {
  isOpen: boolean
  onClose: () => void
}

export default function Sidebar({ isOpen, onClose }: SidebarProps) {
  const pathname = usePathname()
  const { permissions } = useAdmin()
  const [expandedItems, setExpandedItems] = useState<string[]>([])

  // 过滤有权限的菜单项
  const filteredMenu = filterMenuByPermissions(SIDEBAR_MENU, permissions)

  // 自动展开包含当前路径的父菜单
  useEffect(() => {
    const shouldExpand: string[] = []

    filteredMenu.forEach(item => {
      if (item.children && item.children.length > 0) {
        const hasActiveChild = item.children.some(child => pathname === child.href)
        if (hasActiveChild || (pathname.startsWith(item.href) && pathname !== item.href)) {
          shouldExpand.push(item.id)
        }
      }
    })

    setExpandedItems(prev => {
      const newExpanded = [...new Set([...prev, ...shouldExpand])]
      return newExpanded
    })
  }, [pathname, filteredMenu])

  // 切换子菜单展开状态
  const toggleExpanded = (itemId: string) => {
    setExpandedItems(prev => 
      prev.includes(itemId) 
        ? prev.filter(id => id !== itemId)
        : [...prev, itemId]
    )
  }

  // 检查菜单项是否激活
  const isActive = (href: string) => {
    if (href === '/admin' && pathname === '/admin') return true
    if (href !== '/admin' && pathname.startsWith(href)) return true
    return false
  }

  // 渲染菜单项
  const renderMenuItem = (item: MenuItem, level = 0) => {
    const hasChildren = item.children && item.children.length > 0
    const isExpanded = expandedItems.includes(item.id)
    const active = isActive(item.href)

    // 根据层级调整padding
    const paddingClass = level === 0 ? 'px-4' : level === 1 ? 'px-6' : 'px-8'
    const iconSize = level === 0 ? 'w-5 h-5' : 'w-4 h-4'

    return (
      <div key={item.id}>
        {hasChildren ? (
          <button
            onClick={() => {
              console.log('Parent menu clicked:', item.label, item.id)
              toggleExpanded(item.id)
            }}
            className={`flex items-center w-full ${paddingClass} py-3 text-left text-sm font-medium rounded-lg transition-colors cursor-pointer ${
              active
                ? 'bg-blue-100 text-blue-700'
                : 'text-gray-700 hover:bg-gray-100'
            }`}
          >
            <span className="flex-1 flex items-center">
              <MenuIcon name={item.icon} className={`${iconSize} mr-3`} />
              {item.label}
            </span>
            <svg
              className={`w-4 h-4 transition-transform ${isExpanded ? 'rotate-90' : ''}`}
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
            >
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5l7 7-7 7" />
            </svg>
          </button>
        ) : (
          <Link
            href={item.href}
            onClick={(e) => {
              console.log('Menu item clicked:', item.label, item.href)
              onClose()
            }}
            className={`flex items-center w-full ${paddingClass} py-3 text-sm font-medium rounded-lg transition-colors cursor-pointer ${
              active
                ? 'bg-blue-100 text-blue-700'
                : 'text-gray-700 hover:bg-gray-100'
            }`}
          >
            <MenuIcon name={item.icon} className={`${iconSize} mr-3 flex-shrink-0`} />
            <span className="truncate">{item.label}</span>
          </Link>
        )}

        {/* 子菜单 */}
        {hasChildren && isExpanded && (
          <div className="mt-1 space-y-1 relative z-10">
            {item.children?.map(child => renderMenuItem(child, level + 1))}
          </div>
        )}
      </div>
    )
  }

  return (
    <>
      {/* 移动端遮罩 */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black bg-opacity-50 z-40 lg:hidden"
          onClick={onClose}
        />
      )}

      {/* 侧边栏 */}
      <div
        className={`fixed inset-y-0 left-0 z-50 w-64 bg-white shadow-lg transform transition-transform duration-300 ease-in-out lg:translate-x-0 ${
          isOpen ? 'translate-x-0' : '-translate-x-full'
        }`}
      >
        {/* Logo */}
        <div className="flex items-center justify-between h-16 px-4 border-b border-gray-200">
          <Link href="/admin" className="flex items-center space-x-2">
            <div className="h-8 w-8 rounded-lg bg-gradient-to-r from-blue-600 to-purple-600 flex items-center justify-center">
              <span className="text-white font-bold text-sm">Y</span>
            </div>
            <span className="font-heading font-bold text-xl text-gray-900">YXLP</span>
          </Link>
          
          {/* 移动端关闭按钮 */}
          <button
            onClick={onClose}
            className="lg:hidden p-2 rounded-md text-gray-400 hover:text-gray-500 hover:bg-gray-100"
          >
            <svg className="w-6 h-6" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>

        {/* 导航菜单 */}
        <nav className="flex-1 px-4 py-4 space-y-2 overflow-y-auto">
          {filteredMenu.map(item => renderMenuItem(item))}
        </nav>

        {/* 底部信息 */}
        <div className="p-4 border-t border-gray-200">
          <div className="text-xs text-gray-500 text-center">
            YXLP 管理系统 v1.0
          </div>
        </div>
      </div>
    </>
  )
}

